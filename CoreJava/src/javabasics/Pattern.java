public class Pattern
{

    public static void main(String[] args)
    {
       for(int i=1;i<7;i++)
       {
          
            System.out.print("1\t");
             
           if(i!=1)
           {
             
           System.out.print((i+1)+"\t");
                
           }
         
           System.out.print("\t"+i);
       }//external for loop

    }

}
